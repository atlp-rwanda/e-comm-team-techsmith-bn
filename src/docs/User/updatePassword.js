const updateUserPassword = {
  tags: ['User'],
  description: 'Updating user password',
  operationId: 'updatePassword',

  // PARAMETERS
  parameters: [
    {
      name: 'cookie',
      in: 'header',
      schema: {
        type: 'string',
        description: 'Token as generated by Postman',
      },
      required: true,
    },
  ],

  // REQUEST BODY
  requestBody: {
    description: 'Old password to be updated and the new password',
    content: {
      'application/json': {
        schema: {
          $ref: '#/components/schemas/updatePasswordRequest',
        },
      },
    },
  },

  // RESPONSES
  responses: {
    // SUCCESS
    200: {
      description: 'Password updated successfully',
      content: {
        'application/json': {
          schemas: {
            message: 'You have updated your password successfully',
          },
        },
      },
    },
    // UNAUTHORIZED
    401: {
      description: 'Old password not correct',
      content: {
        'application/json': {
          schema: {
            message: 'Old password not correct',
          },
        },
      },
    },
    // BAD REQUEST
    400: {
      description: 'Bad request',
      content: {
        'application/json': {
          schema: {
            message: 'Passwords do not match',
          },
        },
      },
    },
    // SERVER ERROR
    500: {
      description: 'Server error',
      content: {
        'application/json': {
          schema: {
            message: 'Internal server error',
          },
        },
      },
    },
  },
};

export default updateUserPassword;
